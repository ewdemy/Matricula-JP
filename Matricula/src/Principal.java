    public void loadTable(){
        DefaultTableModel modelo = (DefaultTableModel)tblMat.getModel();
        modelo.setNumRows(0);
        
        Arquivo a = new Arquivo();
        int qtd = 0;
        
        for(Matricula m: a.listar()){
            qtd++;
            modelo.addRow(new Object[]{
                    qtd,
                    m.getAluno(),
                    m.getSerie(),
                    m.getData()
            });
        }
        
        lblQtd.setText(String.valueOf(qtd));
    }

    public void loadAluno(){
        DefaultTableModel modelo = (DefaultTableModel)tblMat.getModel();
        modelo.setNumRows(0);
        
        Arquivo a = new Arquivo();
        int qtd = 0;
        for(Matricula m: a.listar()){
             
            if(m.getAluno().equalsIgnoreCase(txtAluno.getText())){
                qtd++;
                modelo.addRow(new Object[]{
                    qtd,
                    m.getAluno(),
                    m.getSerie(),
                    m.getData()
            });        
            }
        } 
        lblQtd.setText(String.valueOf(qtd));
    }
    
