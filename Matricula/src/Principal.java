    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if (cbxOpcao.getSelectedItem().equals("Aluno")){
            if(! txtAluno.getText().isEmpty()){
                loadAluno();
                limpaCampos();
                if (tblMat.getRowCount() == 0){
                    JOptionPane.showMessageDialog(null, "nenhum registro encontrado!");
                    loadTable();
                    limpaCampos();
                }
            }else {
                JOptionPane.showMessageDialog(null, "Preencha o campo aluno para pesquisar!");
            }
            
            txtAluno.setText("");
        }else if(cbxOpcao.getSelectedItem().equals("Serie")){
            loadSerie();
            limpaCampos();
            if (tblMat.getRowCount() == 0){
                    JOptionPane.showMessageDialog(null, "nenhum registro encontrado!");
                    loadTable();
                    limpaCampos();
                }
            cbxOpcao.setSelectedIndex(0);
        }else if(cbxOpcao.getSelectedItem().equals("Data")){
            loadData();
            limpaCampos();
            if (tblMat.getRowCount() == 0){
                    JOptionPane.showMessageDialog(null, "nenhum registro encontrado!");
                    loadTable();
                    limpaCampos();
                }
        }else {
            
            loadTable();
            limpaCampos();
            if (tblMat.getRowCount() == 0){
                    JOptionPane.showMessageDialog(null, "nenhum registro encontrado!");
                }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

