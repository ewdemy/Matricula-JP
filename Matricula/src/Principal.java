    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        Matricula m = new Matricula();
        
        
        if(! txtAluno.getText().isEmpty()){
            m.setAluno(txtAluno.getText());
            m.setSerie(cbxSerie.getSelectedItem().toString());
            m.setData(txtData.getText());

            Arquivo arq = new Arquivo();

            List<Matricula> matriculas = arq.listar();

            arq.deletar();
            for(Matricula mat:matriculas){
                if(! mat.getAluno().equalsIgnoreCase(alterar)){
                    arq.gravar(mat);
                }
            }
            arq.gravar(m);
            JOptionPane.showMessageDialog(null, "Registro alterado com sucesso! ");

            limpaCampos();
            
        
        }else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos! ");
        }
        loadTable();
    }//GEN-LAST:event_btnAlterarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Matricula m = new Matricula();
        
        if(! txtAluno.getText().isEmpty()){
            m.setAluno(txtAluno.getText());
            m.setSerie(cbxSerie.getSelectedItem().toString());
            m.setData(txtData.getText());

            Arquivo arq = new Arquivo();

            List<Matricula> matriculas = arq.listar();

            arq.deletar();
            for(Matricula mat:matriculas){
                if(! mat.getAluno().equalsIgnoreCase(m.getAluno())){
                    arq.gravar(mat);
                }
            }
            JOptionPane.showMessageDialog(null, "Registro excluido com sucesso! ");

            limpaCampos();
            
        
        }else {
            JOptionPane.showMessageDialog(null, "Preencha o campo aluno! ");
        }
        loadTable();
    }//GEN-LAST:event_btnExcluirActionPerformed

